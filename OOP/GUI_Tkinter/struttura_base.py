# Importa la libreria tkinter
import tkinter as tk # importa la libreria tkinter e la rinomina come tk per semplificare l'utilizzo

# Crea la finestra (Root) principale
root = tk.Tk() # crea una finestra principale utilizzando la classe Tk di tkinter, simile a una finestra Desktop Windows

# Personalizza la finestra (Widget)
root.title("La mia prima finestra") # imposta il titolo della finestra a "La mia prima finestra"

# Avvia la finestra (Mainloop)
root.mainloop() # avvia il ciclo principale della finestra, che mantiene la finestra aperta e in attesa di eventi (come clic del mouse o input da tastiera)
